<h2><a name="The County Press">The County Press</a></h2>
<p><b>All praise to the County Press!</b> It chronicles and thereby defines a
large part of our Island life. Those lucky enough to live on the  <a href="http://www.visitisleofwight.co.uk/">Isle of Wight</a> will be familiar with the
weekly appearance of the oldest, indeed only local newspaper on the Island. So
popular is it that (it claims) over 90% of Islanders read it each week.&nbsp;I
cannot imagine that this statistic is untrue.</p>
<p>As well as being a vital conduit of local news it is an extraordinary cultural
experience which is essential material for any
person who wants to gain some sort of insight into life on the Isle of Wight.&nbsp;
The great and lesser doings of all sections of the Island community are set out,
week by week, in great detail.&nbsp;</p>

<p> More than the news, the texture and style
of the County Press is something which is instantly evocative of the Isle of
Wight. Many expatriate Islanders across the world rely upon the postal delivery
of the IWCP to remind them of what is going on on the Island. To read and digest
the County Press is to glimpse in passing the soul of the typical Island
resident - a person, who, of course, does not exist, as no Island person is in
any way typical.</p>

<p>No item of news is too small for the County Press. This makes for intriguing
reading, but also has a strange side effect. Everyone is anxious to reads the
paper, to see who has been up to what. It is a paper which reports not only the
news but the facts which become gossip. No Island person appearing in court for
the most minor misdemeanour can imagine that their neighbours will not hear of
it by way of the Court Reports. Even the most casual remark at any sort of
public gathering can be repeated courtesy of the keen ears of the County Press,
so if someone is rude about someone else, they will usually get to know. Whether
this effect is one which contributes to the more relaxed and convivial nature of
Island life I do not know, but I like to think so.&nbsp;</p>

<h2><a name="This Page and the Author">About this page</a></h2>

<p><b>This page</b>, and the other bits that go with it, are offered as a
tribute to this great newspaper. I have for many years pondered making a collection
of my favourite bits from the County Press, and with this page I shall start
revealing some of the gems of my collection as it develops.</p>
